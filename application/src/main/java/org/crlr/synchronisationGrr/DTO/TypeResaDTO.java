package org.crlr.synchronisationGrr.DTO;

public class TypeResaDTO {

	private String id;
	private String type_name;
	private String order_display;
	private String couleur;
	private String type_letter;
	private String disponible;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType_name() {
		return type_name;
	}
	public void setType_name(String type_name) {
		this.type_name = type_name;
	}
	public String getOrder_display() {
		return order_display;
	}
	public void setOrder_display(String order_display) {
		this.order_display = order_display;
	}
	public String getCouleur() {
		return couleur;
	}
	public void setCouleur(String couleur) {
		this.couleur = couleur;
	}
	public String getType_letter() {
		return type_letter;
	}
	public void setType_letter(String type_letter) {
		this.type_letter = type_letter;
	}
	public String getDisponible() {
		return disponible;
	}
	public void setDisponible(String disponible) {
		this.disponible = disponible;
	}
	
	
}
