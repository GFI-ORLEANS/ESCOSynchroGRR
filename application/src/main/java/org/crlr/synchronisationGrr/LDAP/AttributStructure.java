package org.crlr.synchronisationGrr.LDAP;

public class AttributStructure {

    private String attributCode;
    private String attributNomCourt;
    private String attributNomLong;
    private String attributCodePostal;
    private String attributVille;
    private String attributAdresse;
    
	public String getAttributCode() {
		return attributCode;
	}
	public void setAttributCode(String attributCode) {
		this.attributCode = attributCode;
	}
	public String getAttributNomCourt() {
		return attributNomCourt;
	}
	public void setAttributNomCourt(String attributNomCourt) {
		this.attributNomCourt = attributNomCourt;
	}
	public String getAttributNomLong() {
		return attributNomLong;
	}
	public void setAttributNomLong(String attributNomLong) {
		this.attributNomLong = attributNomLong;
	}
	public String getAttributCodePostal() {
		return attributCodePostal;
	}
	public void setAttributCodePostal(String attributCodePostal) {
		this.attributCodePostal = attributCodePostal;
	}
	public String getAttributVille() {
		return attributVille;
	}
	public void setAttributVille(String attributVille) {
		this.attributVille = attributVille;
	}
	public String getAttributAdresse() {
		return attributAdresse;
	}
	public void setAttributAdresse(String attributAdresse) {
		this.attributAdresse = attributAdresse;
	}
    
    
	
}
